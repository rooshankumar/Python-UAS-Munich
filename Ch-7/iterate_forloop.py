# List
print("List:")
l = [1, 4, 64, 234, 4445]
for item in l:
    print(item)

print("\nTuple:")
# Tuple
t = (1, 23, 45, 678, 5422)
for item in t:
    print(item)

print("\nSet:")
# Set
s = {10, 20, 30, 40, 50}
for item in s:
    print(item)

print("\nDictionary:")
# Dictionary
d = {"name": "Roshan", "age": 25, "city": "Patna"}
for key in d:
    print(key, ":", d[key])

print("\nString:")
# String
str_data = "Hello"
for char in str_data:
    print(char)
